export function useI18n() {
  const locale = computed({
    get: () => currentLocale.value,
    set: (val: Locale) => {
      currentLocale.value = val
      localStorage.setItem('occ-locale', val)
    }
  })

  // 从 localStorage 恢复
  const saved = localStorage.getItem('occ-locale')
  if (saved && ['zh', 'en', 'es'].includes(saved)) {
    currentLocale.value = saved as Locale
  }

  function t(key: string, params?: Record<string, string>): string {
    let msg = messages[currentLocale.value][key] || key
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        msg = msg.replace(`{${k}}`, v)
      })
    }
    return msg
  }

  return { t, locale }
}
